// Lesson size: 3 new items per lesson (research: 3-5 optimal)
export const LESSON_SIZE = 3

/**
 * Split a stage's items into lessons of LESSON_SIZE.
 */
export function getLessons(stage) {
  const items = stage.items
  const lessons = []
  for (let i = 0; i < items.length; i += LESSON_SIZE) {
    const chunk = items.slice(i, i + LESSON_SIZE)
    const preview = chunk.map(l => l.word).join(', ')
    lessons.push({
      index: lessons.length,
      items: chunk,
      title: `Lesson ${lessons.length + 1}`,
      preview,
    })
  }
  return lessons
}

/**
 * Get all items from previous lessons in the same stage (for interleaved review).
 */
export function getPreviousItems(stage, lessonIdx) {
  const lessons = getLessons(stage)
  return lessons.slice(0, lessonIdx).flatMap(l => l.items)
}

